package examples;

public class Cm_mm_km {
	public static void main(String args[]) {
		double num=10000;
	    System.out.println("Length in centimeter is : " + num);
	    System.out.println("Length in meter is : " + (num/100));
	    System.out.println("Length in kilometer is : " + (num/1000));
	}
}
