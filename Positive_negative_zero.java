package examples;

public class Positive_negative_zero {
	public static void main(String args[]) {
		double num=-60;
	    if(num==0){
	        System.out.println("Number is zero");
	    }
	    else if(num>=0){
	    	System.out.println("Number is positive");
	    }
	    else
	    	System.out.println("Number is negative");
	}
}
