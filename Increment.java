
public class Increment {
	public static void main(String[] args) {
		int num=57;
		System.out.println("post increment by 1 the number is : " + ++num);
		System.out.println("pre increment by 1 the number is : " + ++num);
		System.out.println("increasing by 1 the number is : " + ++num);
		System.out.println("post decrement by 1 the number is : " + --num);
		System.out.println("pre decrement by 1 the number is : " + --num);
		System.out.println("decreasing by 1 the number is : " + --num);
	}
}
