public class Area_Perimeter_of_Rectangle {
	public static void main(String args[]) {
		int l=10,b=15, periofreact, areaofreact;
	    areaofreact = l*b;
	    periofreact = 2*(l+b);
	    System.out.println("Lenght of reactangle is : " + l);
	    System.out.println("Width of reactangle is : " + b);
	    System.out.println("Perimeter of reactangle is : " + periofreact);
	    System.out.println("Area of reactangle is : " + areaofreact);
	}
}
