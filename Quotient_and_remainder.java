package examples;

public class Quotient_and_remainder {
	public static void main(String args[]) {
		int x=25, y=3;
	    System.out.println("First number is : " + x);
	    System.out.println("Second number is : " + y);
	    System.out.println("The quotient of the divison is : " + x/y);
	    System.out.println("The remainder of the division is : " + x%y);
	}
}
