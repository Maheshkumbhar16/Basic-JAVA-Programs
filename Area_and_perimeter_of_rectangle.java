package examples;

public class Area_and_perimeter_of_rectangle {
	public static void main(String args[]) {
		double leng=8.5, brdth=5.6;
	    System.out.println("Width of the rectangle is : " + leng);
	    System.out.println("Height of the rectangle is : " + brdth);
	    System.out.println("Area of the rectangle is : " + (leng*brdth));
	    System.out.println("Perimeter of the rectangle is : "  + (2*(leng+brdth)));
	}
}
